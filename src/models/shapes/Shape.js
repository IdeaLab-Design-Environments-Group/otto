/**
 * @fileoverview Abstract base class for every shape in the Otto parametric design system.
 *
 * Shape is schema-driven: each concrete subclass declares
 *
 *   static type   = 'circle';            // registered type key / JSON discriminator
 *   static SCHEMA = { radius: {...} };   // property descriptors (see schema.js)
 *
 * and implements only its geometry methods (toGeometryPath, render,
 * containsPoint, getBounds). Everything property-shaped — construction with
 * defaults and aliases, the bindable-property list, resolve(), clone(),
 * translate(), toJSON()/fromJSON() — is implemented once here, driven by the
 * merged schema ({@link Shape.fullSchema} = class SCHEMA + COMMON_SCHEMA).
 *
 * The bindings map connects property names to Binding objects (parameter,
 * expression, or literal). resolve() — called once per frame by the renderer —
 * clones the shape and overwrites every bound property with its evaluated
 * value; the original stored shape is never mutated during rendering.
 *
 * Serialization contract (wire format 1.0.0-compatible):
 *   { id, type, position, bindings, <schema props in declaration order> }
 * Literal values are written for unbound properties; bound properties are
 * represented by their binding JSON instead (unless `alwaysSerialize`).
 * Deserialization goes through ShapeRegistry.fromJSON, which dispatches to
 * the class and then restores bindings via BindingRegistry.
 *
 * @module models/shapes/Shape
 */
import { COMMON_SCHEMA, resolvePropertyValue } from './schema.js';

/**
 * Abstract base class for all Otto shapes.
 *
 * Cannot be instantiated directly. Concrete subclasses must define
 * `static type`, `static SCHEMA`, and the geometry methods
 * {@link Shape#getBounds}, {@link Shape#containsPoint}, {@link Shape#render}
 * (plus `toGeometryPath()` by convention).
 *
 * @abstract
 * @class
 */
export class Shape {
    /**
     * Registered type key (e.g. 'circle'). Concrete classes MUST override.
     * Used as this.type and as the ShapeRegistry registration key.
     * @type {?string}
     */
    static type = null;

    /**
     * Property descriptors for this class (see schema.js). Concrete classes
     * override; the base contributes {@link COMMON_SCHEMA} via fullSchema.
     * @type {Object.<string, Object>}
     */
    static SCHEMA = {};

    /**
     * Merged schema: the class's own SCHEMA first (declaration order controls
     * serialization key order), then the common properties. Cached per class.
     * @returns {Object.<string, Object>}
     */
    static get fullSchema() {
        if (!Object.prototype.hasOwnProperty.call(this, '_fullSchema')) {
            this._fullSchema = Object.freeze({ ...this.SCHEMA, ...COMMON_SCHEMA });
        }
        return this._fullSchema;
    }

    /**
     * Construct a shape from an options bag.
     *
     * Every schema property is resolved from `options` in priority order:
     * direct name → aliases → descriptor default (which may be a function of
     * the options, letting geometry default to `options.position`). Values
     * pass through the descriptor's `copy` function when present, so callers
     * keep ownership of arrays/objects they pass in.
     *
     * @param {string} id - Unique, human-readable identifier (e.g. "Circle 3"),
     *   generated by ShapeRegistry.generateId and preserved across save/load.
     * @param {Object} [options={}] - Property values, aliases, and `position`
     *   ({x, y} legacy drop position; kept for serialization compatibility,
     *   not used for geometry).
     */
    constructor(id, options = {}) {
        if (this.constructor === Shape) {
            throw new Error('Shape is an abstract class and cannot be instantiated directly');
        }
        if (!this.constructor.type) {
            throw new Error(`${this.constructor.name} must define a static type`);
        }
        /** @type {string} Unique, human-readable identifier (e.g. "Circle 3"). */
        this.id = id;
        /** @type {string} Registered type key matching the ShapeRegistry entry. */
        this.type = this.constructor.type;
        /** @type {{x: number, y: number}} Legacy position; shallow-copied to avoid shared references. */
        this.position = { ...(options.position ?? { x: 0, y: 0 }) };
        /**
         * @type {Object.<string, import('../Binding.js').Binding>}
         * Map from bindable property name to its active Binding object.
         * Properties absent from this map use their literal values.
         */
        this.bindings = {};

        for (const [prop, desc] of Object.entries(this.constructor.fullSchema)) {
            const value = resolvePropertyValue(prop, desc, options);
            this[prop] = desc.copy ? desc.copy(value) : value;
        }
    }

    /**
     * Property names that can be driven by parameter bindings, in schema
     * declaration order. This drives the Properties Panel field list,
     * {@link Shape#resolve}, and {@link Shape#toJSON}.
     *
     * @returns {string[]} e.g. ['centerX', 'centerY', 'radius', 'rotation'].
     */
    getBindableProperties() {
        const schema = this.constructor.fullSchema;
        return Object.keys(schema).filter(prop => schema[prop].bindable);
    }

    /**
     * Look up the PropertyDescriptor for a property (UI hint access for the
     * Properties Panel / resize strategies).
     *
     * @param {string} property
     * @returns {?Object} The descriptor, or null if not a schema property.
     */
    getPropertyDescriptor(property) {
        return this.constructor.fullSchema[property] ?? null;
    }

    /**
     * Template Method — produce a fully-resolved copy of this shape with all
     * parameter bindings evaluated to their current values.
     *
     * Called once per frame by the renderer for every shape in the scene.
     * The stored shape is never mutated; the returned clone has each bound
     * property overwritten with the value from its binding.
     *
     * @param {Object} parameterStore - Passed for context; value lookup is
     *   delegated to bindingResolver.
     * @param {Object} bindingResolver - Evaluates a Binding into a concrete
     *   value. May be null (unit tests / init); bound properties then retain
     *   their literal values.
     * @returns {Shape} A resolved clone, safe to render and hit-test.
     */
    resolve(parameterStore, bindingResolver) {
        const resolved = this.clone();

        this.getBindableProperties().forEach(property => {
            const binding = this.bindings[property];
            if (binding && bindingResolver) {
                resolved[property] = bindingResolver.resolveValue(binding);
            } else if (this[property] !== undefined) {
                resolved[property] = this[property];
            }
        });

        return resolved;
    }

    /**
     * Compute the axis-aligned bounding box that encloses this shape.
     * Used for culling, selection quick-rejection, and the Properties Panel.
     *
     * @abstract
     * @returns {{x: number, y: number, width: number, height: number}}
     * @throws {Error} Always, if not overridden.
     */
    getBounds() {
        throw new Error('getBounds() must be implemented by subclass');
    }

    /**
     * Whether the canvas-space point lies inside (or on the stroke of) this
     * shape. Used by the selection / drag system.
     *
     * @abstract
     * @param {number} x
     * @param {number} y
     * @returns {boolean}
     * @throws {Error} Always, if not overridden.
     */
    containsPoint(x, y) {
        throw new Error('containsPoint() must be implemented by subclass');
    }

    /**
     * Draw this shape onto the given 2D canvas rendering context.
     *
     * @abstract
     * @param {CanvasRenderingContext2D} ctx
     * @throws {Error} Always, if not overridden.
     */
    render(ctx) {
        throw new Error('render() must be implemented by subclass');
    }

    /**
     * Snapshot every schema property (plus position) into a plain options bag
     * suitable for reconstructing this shape via the constructor. Values pass
     * through the descriptor `copy` functions, so the result shares no
     * references with this instance.
     *
     * @returns {Object} Options accepted by `new this.constructor(id, options)`.
     */
    toOptions() {
        const options = { position: { ...this.position } };
        for (const [prop, desc] of Object.entries(this.constructor.fullSchema)) {
            options[prop] = desc.copy ? desc.copy(this[prop]) : this[prop];
        }
        return options;
    }

    /**
     * Deep-copy this shape, including all current property values AND all
     * active bindings. Workhorse of {@link Shape#resolve} and duplication.
     *
     * @returns {Shape} A new instance of the same concrete class.
     */
    clone() {
        const copy = new this.constructor(this.id, this.toOptions());
        for (const property of Object.keys(this.bindings)) {
            copy.setBinding(property, this.bindings[property]);
        }
        return copy;
    }

    /**
     * Shift this shape by (dx, dy): every schema property with a `translate`
     * role moves accordingly. Replaces the old per-type "center-based vs
     * origin-based" branching in drag/move code. Shapes whose geometry lives
     * outside scalar properties (PathShape's point array) override this.
     *
     * @param {number} dx
     * @param {number} dy
     * @returns {Shape} this, for chaining.
     */
    translate(dx, dy) {
        for (const [prop, desc] of Object.entries(this.constructor.fullSchema)) {
            if (desc.translate === 'x') {
                this[prop] += dx;
            } else if (desc.translate === 'y') {
                this[prop] += dy;
            }
        }
        return this;
    }

    /**
     * Attach a parameter Binding to a bindable property. After this call the
     * property's render-time value is driven by the binding, not the literal.
     *
     * @param {string} property - Must be in {@link Shape#getBindableProperties}.
     * @param {import('../Binding.js').Binding} binding
     * @throws {Error} If the property is not bindable for this shape type.
     */
    setBinding(property, binding) {
        if (!this.getBindableProperties().includes(property)) {
            throw new Error(`Property ${property} is not bindable for ${this.type}`);
        }
        this.bindings[property] = binding;
    }

    /**
     * The active Binding for a property, or null if it uses its literal value.
     *
     * @param {string} property
     * @returns {import('../Binding.js').Binding|null}
     */
    getBinding(property) {
        return this.bindings[property] || null;
    }

    /**
     * Serialize this shape into a plain JSON-safe object.
     *
     * Writes id/type/position, every active binding (via Binding.toJSON()),
     * then every schema property in declaration order subject to descriptor
     * flags: bound properties are skipped unless `alwaysSerialize`; values
     * equal to a static default are skipped when `omitIfDefault`; null values
     * are skipped when `omitIfNull`; `serialize` transforms apply last.
     *
     * @returns {Object} A plain object ready for JSON.stringify().
     */
    toJSON() {
        const json = {
            id: this.id,
            type: this.type,
            position: { ...this.position },
            bindings: {}
        };

        Object.keys(this.bindings).forEach(property => {
            json.bindings[property] = this.bindings[property].toJSON();
        });

        for (const [prop, desc] of Object.entries(this.constructor.fullSchema)) {
            const value = this[prop];
            if (value === undefined) {
                continue;
            }
            if (this.bindings[prop] && !desc.alwaysSerialize) {
                continue;
            }
            if (desc.omitIfDefault && typeof desc.default !== 'function' && value === desc.default) {
                continue;
            }
            if (desc.omitIfNull && value == null) {
                continue;
            }
            json[prop] = desc.serialize ? desc.serialize(value) : value;
        }

        return json;
    }

    /**
     * Reconstruct a shape of this concrete class from its serialized JSON.
     * The JSON doubles as the constructor options bag: schema properties are
     * picked out by name (with aliases and defaults for anything missing).
     *
     * Bindings are NOT restored here — {@link ShapeRegistry.fromJSON} does
     * that afterwards via BindingRegistry, and is the entry point all
     * deserialization must go through (it also dispatches on json.type).
     *
     * @param {Object} json - As produced by {@link Shape#toJSON}.
     * @returns {Shape}
     */
    static fromJSON(json) {
        if (this === Shape) {
            throw new Error('Use ShapeRegistry.fromJSON() instead');
        }
        return new this(json.id, json);
    }
}
